function solve(data, criteria) {

    data = JSON.parse(data);

    let [prop, value] = criteria.split('-');

    let counter = 0;

    for (let i = 0; i < data.length; i ++) {

        let person = data[i];

        if (prop == 'all') {

            console.log(`${counter}. ${person.first_name} ${person.last_name} - ${person.email}`);

            counter ++;

            continue;
        }

        if (person[prop] == value) {

            console.log(`${counter}. ${person.first_name} ${person.last_name} - ${person.email}`);

            counter ++;
        }
    }
}
